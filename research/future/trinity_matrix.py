def standard_matmul(A, B):
    """–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ —É–º–Ω–æ–∂–µ–Ω–∏–µ –º–∞—Ç—Ä–∏—Ü"""
    n = len(A)
    m = len(B[0])
    k = len(B)
    
    C = [[0] * m for _ in range(n)]
    
    for i in range(n):
        for j in range(m):
            for l in range(k):
                C[i][j] += A[i][l] * B[l][j]
    
    return C

def ternary_matmul(A_ternary, B, scale=1.0):
    """
    –£–º–Ω–æ–∂–µ–Ω–∏–µ —Å ternary –≤–µ—Å–∞–º–∏ A ‚àà {-1, 0, +1}
    
    –ù–µ—Ç —É–º–Ω–æ–∂–µ–Ω–∏–π! –¢–æ–ª—å–∫–æ —Å–ª–æ–∂–µ–Ω–∏—è –∏ –≤—ã—á–∏—Ç–∞–Ω–∏—è.
    """
    n = len(A_ternary)
    m = len(B[0])
    k = len(B)
    
    C = [[0.0] * m for _ in range(n)]
    
    for i in range(n):
        for j in range(m):
            total = 0.0
            for l in range(k):
                if A_ternary[i][l] == 1:
                    total += B[l][j]
                elif A_ternary[i][l] == -1:
                    total -= B[l][j]
                # A[i][l] == 0: –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º
            C[i][j] = total * scale
    
    return C

def ternarize_matrix(A, threshold_ratio=0.7):
    """–ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –º–∞—Ç—Ä–∏—Ü—ã –≤ ternary"""
    n = len(A)
    m = len(A[0])
    
    # –í—ã—á–∏—Å–ª—è–µ–º –ø–æ—Ä–æ–≥
    flat = [abs(A[i][j]) for i in range(n) for j in range(m)]
    mean_abs = sum(flat) / len(flat)
    threshold = threshold_ratio * mean_abs
    
    # Ternarize
    T = [[0] * m for _ in range(n)]
    for i in range(n):
        for j in range(m):
            if A[i][j] > threshold:
                T[i][j] = 1
            elif A[i][j] < -threshold:
                T[i][j] = -1
            else:
                T[i][j] = 0
    
    # –í—ã—á–∏—Å–ª—è–µ–º scale
    non_zero = [abs(A[i][j]) for i in range(n) for j in range(m) 
                if T[i][j] != 0]
    scale = sum(non_zero) / len(non_zero) if non_zero else 1.0
    
    return T, scale

